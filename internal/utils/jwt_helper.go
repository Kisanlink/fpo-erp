// HasPermission checks if user has a specific permission
func HasPermission(claims *CustomClaims, permission string) bool {
	for _, perm := range claims.Permissions {
		if perm == permission {
			return true
		}
	}
	return false
}

// HasScope checks if user has a specific scope
func HasScope(claims *CustomClaims, scope string) bool {
	for _, s := range claims.Scopes {
		if s == scope {
			return true
		}
	}
	return false
}

// GetOrganizationName returns the name of the primary organization
func GetOrganizationName(claims *CustomClaims) string {
	if len(claims.Organizations) == 0 {
		return ""
	}
	return claims.Organizations[0].Name
}

// GetActiveRoles returns all active roles for the user
func GetActiveRoles(claims *CustomClaims) []RoleContext {
	activeRoles := make([]RoleContext, 0)
	for _, role := range claims.Roles {
		if role.IsActive {
			activeRoles = append(activeRoles, role)
		}
	}
	return activeRoles
}

// GetRoleNames returns names of all active roles
func GetRoleNames(claims *CustomClaims) []string {
	activeRoles := GetActiveRoles(claims)
	names := make([]string, len(activeRoles))
	for i, role := range activeRoles {
		names[i] = role.Name
	}
	return names
}

// HasRole checks if user has a specific role (by name)
func HasRole(claims *CustomClaims, roleName string) bool {
	for _, role := range claims.Roles {
		if role.IsActive && role.Name == roleName {
			return true
		}
	}
	return false
}
